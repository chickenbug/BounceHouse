package model;

public class AutoBid {
	public int autoBidID;
	public int auctionID;
	public int userID;
	public float maxBid;
	
	public AutoBid(int autoBidID, int auctionID, int userID, float maxBid){
		this.auctionID = autoBidID;
		this.auctionID = auctionID;
		this.userID = userID;
		this.maxBid = maxBid;
	}
	

}
